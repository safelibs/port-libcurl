def parse_makefile(path: pathlib.Path) -> dict:
    raw_text = path.read_text()
    logical_lines: list[str] = []
    buffer = ""
    for raw_line in raw_text.splitlines():
        line = raw_line.rstrip()
        if not buffer and (not line.strip() or line.lstrip().startswith("#")):
            continue
        if buffer:
            buffer += " " + line.lstrip()
        else:
            buffer = line
        if buffer.endswith("\\"):
            buffer = buffer[:-1].rstrip()
            continue
        logical_lines.append(buffer.strip())
        buffer = ""

    variables: dict[str, str] = {}
    assignments: list[dict] = []
    for line in logical_lines:
        match = ASSIGNMENT_RE.match(line)
        if not match:
            continue
        name, operator, value = match.groups()
        value = value.strip()
        if operator == "+=" and name in variables:
            variables[name] = f"{variables[name]} {value}".strip()
        else:
            variables[name] = value
        assignments.append(
            {
                "name": name,
                "operator": operator,
                "value": value,
            }
        )

    return {
        "path": str(path.relative_to(DEFAULT_REPO_ROOT)),
        "raw_text": raw_text,
        "variables": variables,
        "assignments": assignments,
    }


def resolve_make_value(name: str, variables: dict[str, str], seen: set[str] | None = None) -> str:
    if seen is None:
        seen = set()
    if name in seen:
        return variables.get(name, "")
    seen = set(seen)
    seen.add(name)
    value = variables.get(name, "")

    def replace(match: re.Match[str]) -> str:
        ref_name = match.group(1)
        if ref_name in variables:
            return resolve_make_value(ref_name, variables, seen)
        return match.group(0)

    previous = None
    while previous != value:
        previous = value
        value = VAR_REF_RE.sub(replace, value)
    return normalize_whitespace(value)


def tokenize_make_value(value: str) -> list[str]:
    if not value:
        return []
    return [token for token in normalize_whitespace(value).split(" ") if token]


def strip_preprocessor_directives(text: str) -> str:
    kept_lines: list[str] = []
    skip_continuation = False
    for raw_line in text.splitlines():
        line = raw_line.rstrip()
        if skip_continuation:
            skip_continuation = line.endswith("\\")
            continue
        if line.lstrip().startswith("#"):
            skip_continuation = line.endswith("\\")
            continue
        kept_lines.append(raw_line)
    return "\n".join(kept_lines)


def collect_function_declarations(header_dir: pathlib.Path) -> list[dict]:
    declarations: list[dict] = []
    for header_path in sorted(header_dir.glob("*.h")):
        text = strip_preprocessor_directives(BLOCK_COMMENT_RE.sub("", header_path.read_text()))
        current: list[str] = []
        for raw_line in text.splitlines():
            line = raw_line.strip()
            if not current:
                if "CURL_EXTERN" not in line:
                    continue
                current.append(line)
            else:
                if line:
                    current.append(line)
            if ";" not in line:
                continue
            declaration = normalize_whitespace(" ".join(current))
            declaration = declaration.split(";", 1)[0] + ";"
            if "(" in declaration and ")" in declaration:
                declarations.append(
                    {
                        "header": header_path.name,
                        "declaration": declaration,
                    }
                )
            current = []
    return declarations


def collect_struct_names(header_dir: pathlib.Path) -> list[str]:
    names: set[str] = set()
    patterns = [
        re.compile(r"typedef\s+struct\s+[A-Za-z0-9_]+\s+([A-Za-z0-9_]+)\s*;"),
        re.compile(r"typedef\s+struct\s+[A-Za-z0-9_]+\s*\{.*?\}\s*([A-Za-z0-9_]+)\s*;", re.S),
        re.compile(r"struct\s+([A-Za-z0-9_]+)\s*\{"),
        re.compile(r"struct\s+([A-Za-z0-9_]+)\s*;"),
    ]
    for header_path in sorted(header_dir.glob("*.h")):
        text = header_path.read_text()
        for pattern in patterns:
            for match in pattern.finditer(text):
                names.add(match.group(1))
    return sorted(names)


def parse_enum_blocks(text: str) -> list[tuple[str, str]]:
    pattern = re.compile(
        r"typedef\s+enum(?:\s+[A-Za-z0-9_]+)?\s*\{(.*?)\}\s*([A-Za-z0-9_]+)\s*;",
        re.S,
    )
    return [(body, name) for body, name in pattern.findall(text)]


def collect_enum_discriminants(header_dir: pathlib.Path) -> list[dict]:
    results: list[dict] = []
    for header_path in sorted(header_dir.glob("*.h")):
        text = BLOCK_COMMENT_RE.sub("", header_path.read_text())
        for body, enum_name in parse_enum_blocks(text):
            current_numeric = None
            for item in body.split(","):
                entry = item.strip()
                if not entry:
                    continue
                if "=" in entry:
                    name, expression = [part.strip() for part in entry.split("=", 1)]
                    resolved = None
                    try:
                        resolved = int(expression, 0)
                        current_numeric = resolved
                    except ValueError:
                        current_numeric = None
                    results.append(
                        {
                            "header": header_path.name,
                            "enum": enum_name,
                            "name": name,
                            "value_expression": expression,
                            "resolved_value": resolved,
                        }
                    )
                else:
                    if current_numeric is not None:
                        current_numeric += 1
                    results.append(
                        {
                            "header": header_path.name,
                            "enum": enum_name,
                            "name": entry,
                            "value_expression": None,
                            "resolved_value": current_numeric,
                        }
                    )
    return results


def collect_macro_aliases(header_dir: pathlib.Path) -> list[dict]:
    aliases: list[dict] = []
    for header_path in sorted(header_dir.glob("*.h")):
        for raw_line in header_path.read_text().splitlines():
            line = raw_line.strip()
            if not line.startswith("#define "):
                continue
            _, _, rest = line.partition(" ")
            name, _, value = rest.partition(" ")
            if "(" in name:
                continue
            if not re.match(r"^(CURL|CURLOT|CURLOPT|CURLINFO|CURLM|CURLSH|CURLU|LIBCURL)", name):
                continue
            value = value.strip()
            if not value:
                continue
            aliases.append(
                {
                    "header": header_path.name,
                    "name": name,
                    "value": value,
                    "is_alias": bool(re.search(r"[A-Za-z_][A-Za-z0-9_]*", value)),
                }
            )
    return aliases


def parse_version_info(curlver_path: pathlib.Path) -> dict:
    info: dict[str, str] = {}
    for raw_line in curlver_path.read_text().splitlines():
        if not raw_line.startswith("#define "):
            continue
        parts = raw_line.split(None, 2)
        if len(parts) == 3 and parts[1].startswith("LIBCURL_"):
            info[parts[1]] = parts[2]
    return info


def parse_easy_options(options_path: pathlib.Path, easyoptions_path: pathlib.Path) -> dict:
    options_text = options_path.read_text()
    easyoptions_text = easyoptions_path.read_text()
    entries: list[dict] = []
    for match in re.finditer(
        r'\{\s*"([^"]+)"\s*,\s*([A-Z0-9_]+)\s*,\s*([A-Z0-9_]+)\s*,\s*([A-Z0-9_|()<>+-]+)\s*\}',
        easyoptions_text,
        re.S,
    ):
        name, option_id, option_type, flags = match.groups()
        entries.append(
            {
                "name": name,
                "id": option_id,
                "type": option_type,
                "flags": flags,
                "is_alias": "CURLOT_FLAG_ALIAS" in flags,
            }
        )
    return {
        "options_header": str(options_path.relative_to(DEFAULT_REPO_ROOT)),
        "easyoptions_source": str(easyoptions_path.relative_to(DEFAULT_REPO_ROOT)),
        "easytypes": collect_enum_discriminants_from_text(options_text, "options.h"),
        "entries": entries,
    }


