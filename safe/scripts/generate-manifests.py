ASSIGNMENT_RE = re.compile(r"^([A-Za-z0-9_]+)\s*(\+?=)\s*(.*)$")
VAR_REF_RE = re.compile(r"\$\(([A-Za-z0-9_]+)\)")
BLOCK_COMMENT_RE = re.compile(r"/\*.*?\*/", re.S)


def run_git(repo_root: pathlib.Path, *args: str) -> list[str]:
    result = subprocess.run(
        ["git", "-C", str(repo_root), *args],
        check=True,
        text=True,
        stdout=subprocess.PIPE,
    )
    return [line for line in result.stdout.splitlines() if line]


def sha256_path(path: pathlib.Path) -> str:
    return hashlib.sha256(path.read_bytes()).hexdigest()


def normalize_whitespace(value: str) -> str:
    return " ".join(value.split())


def parse_symbols(symbols_path: pathlib.Path) -> dict:
    symbol_versions: list[dict] = []
    exports: list[str] = []
    namespace = None
    soname = None
    package = None
    for index, raw_line in enumerate(symbols_path.read_text().splitlines()):
        line = raw_line.strip()
        if not line:
            continue
        if index == 0:
            parts = line.split()
            soname, package = parts[0], parts[1]
            continue
        if line.startswith("*"):
            continue
        token, _, min_version = line.partition(" ")
        name, _, version = token.partition("@")
        if name == "HIDDEN":
            continue
        if name.startswith("CURL_") and name == version:
            namespace = name
            continue
        if name.startswith("curl_"):
            exports.append(name)
            symbol_versions.append(
                {
                    "symbol": name,
                    "namespace": version,
                    "first_version": min_version.strip(),
                }
            )
    return {
        "symbols_path": str(symbols_path.relative_to(DEFAULT_REPO_ROOT)),
        "package": package,
        "soname": soname,
        "namespace": namespace,
        "symbols": exports,
        "symbol_versions": symbol_versions,
    }


def render_version_script(symbol_manifest: dict) -> str:
    lines = [f"{symbol_manifest['namespace']} {{", "  global:"]
    for symbol in symbol_manifest["symbols"]:
        lines.append(f"    {symbol};")
    lines.extend(["  local:", "    *;", "};", ""])
    return "\n".join(lines)


