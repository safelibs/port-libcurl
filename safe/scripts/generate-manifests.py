def parse_disabled(disabled_path: pathlib.Path) -> dict:
    raw_text = disabled_path.read_text()
    entries: list[dict] = []
    per_test: dict[str, dict] = {}
    condition_stack: list[str] = []
    for line_number, raw_line in enumerate(raw_text.splitlines(), start=1):
        stripped = raw_line.strip()
        if not stripped or stripped.startswith("#"):
            continue
        if stripped.startswith("%if "):
            condition_stack.append(stripped[4:].strip())
            continue
        if stripped == "%endif":
            if condition_stack:
                condition_stack.pop()
            continue
        if stripped == "%else":
            continue
        if stripped.isdigit():
            entry = {
                "line": line_number,
                "test_id": stripped,
                "conditions": list(condition_stack),
            }
            entries.append(entry)
            per_test.setdefault(
                stripped,
                {
                    "must_run_via_runtests_pl": True,
                    "conditional_blocks": [],
                    "selected_flavor_skip": {
                        "openssl": False,
                        "gnutls": False,
                    },
                    "discharged_elsewhere": [],
                },
            )
            if condition_stack:
                per_test[stripped]["conditional_blocks"].append(list(condition_stack))
            else:
                per_test[stripped]["selected_flavor_skip"]["openssl"] = True
                per_test[stripped]["selected_flavor_skip"]["gnutls"] = True
    return {
        "path": str(disabled_path.relative_to(DEFAULT_REPO_ROOT)),
        "raw_text": raw_text,
        "entries": entries,
        "per_test": per_test,
    }


def build_component_common(makefile_am: dict, makefile_inc: dict, extra_keys: Iterable[str] = ()) -> dict:
    merged = {**makefile_inc["variables"], **makefile_am["variables"]}
    keys = ["AM_CPPFLAGS", "AM_CFLAGS", "AM_LDFLAGS", "LDADD", "LIBS", *extra_keys]
    common = {}
    for key in keys:
        if key in merged:
            common[key] = {
                "raw": merged[key],
                "resolved": resolve_make_value(key, merged),
            }
    return common


def build_target_entry(
    component: str,
    target_name: str,
    merged_vars: dict[str, str],
    common: dict,
    role: str,
) -> dict:
    fields = {}
    for suffix in ["SOURCES", "CPPFLAGS", "CFLAGS", "LDFLAGS", "LDADD", "DEPENDENCIES"]:
        key = f"{target_name}_{suffix}"
        if key in merged_vars:
            fields[suffix.lower()] = {
                "raw": merged_vars[key],
                "resolved": resolve_make_value(key, merged_vars),
            }
    sources = tokenize_make_value(fields.get("sources", {}).get("resolved", ""))
    return {
        "target_id": f"{component}:{target_name}",
        "component": component,
        "name": target_name,
        "role": role,
        "common": common,
        "fields": fields,
        "sources": sources,
    }


def extract_rule(raw_text: str, rule_head: str) -> str | None:
    pattern = re.compile(rf"^{re.escape(rule_head)}:.*?(?=^\S|\Z)", re.M | re.S)
    match = pattern.search(raw_text)
    if not match:
        return None
    return match.group(0).rstrip()


def canonicalize_lib_path(component_root: pathlib.Path, token: str) -> pathlib.Path | None:
    if not token.endswith((".c", ".h", ".pl", ".pm", ".md", ".ini", ".in", ".rc")):
        return None
    candidate = (component_root / token).resolve()
    try:
        candidate.relative_to(DEFAULT_REPO_ROOT.resolve())
    except ValueError:
        return None
    return candidate


def collect_include_closure(initial_paths: Iterable[pathlib.Path], tracked: set[pathlib.Path]) -> set[pathlib.Path]:
    resolved = set(initial_paths)
    queue = collections.deque(initial_paths)
    include_re = re.compile(r'^\s*#\s*include\s+"([^"]+)"', re.M)
    while queue:
        path = queue.popleft()
        if not path.exists():
            continue
        text = path.read_text(errors="ignore")
        for include in include_re.findall(text):
            include_path = (path.parent / include).resolve()
            if include_path in tracked and include_path not in resolved:
                resolved.add(include_path)
                queue.append(include_path)
    return resolved


