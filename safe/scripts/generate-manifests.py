def build_abi_manifest(repo_root: pathlib.Path, safe_dir: pathlib.Path) -> dict:
    openssl_symbols = parse_symbols(repo_root / "original/debian/libcurl4t64.symbols")
    gnutls_symbols = parse_symbols(repo_root / "original/debian/libcurl3t64-gnutls.symbols")
    header_hashes = {
        path.name: sha256_path(path)
        for path in sorted((safe_dir / "include" / "curl").glob("*.h"))
    }
    return {
        "schema_version": 1,
        "version_strings": parse_version_info(repo_root / "original/include/curl/curlver.h"),
        "shared_libraries": {
            "openssl": {
                "filename": openssl_symbols["soname"],
                "soname": openssl_symbols["soname"],
                "namespace": openssl_symbols["namespace"],
            },
            "gnutls": {
                "filename": gnutls_symbols["soname"],
                "soname": gnutls_symbols["soname"],
                "namespace": gnutls_symbols["namespace"],
            },
        },
        "symbols": {
            "openssl": openssl_symbols["symbol_versions"],
            "gnutls": gnutls_symbols["symbol_versions"],
        },
        "header_hashes": header_hashes,
        "public_function_declarations": collect_function_declarations(safe_dir / "include" / "curl"),
        "public_struct_names": collect_struct_names(safe_dir / "include" / "curl"),
        "public_enum_discriminants": collect_enum_discriminants(safe_dir / "include" / "curl"),
        "public_macro_aliases": collect_macro_aliases(safe_dir / "include" / "curl"),
        "option_metadata": parse_easy_options(
            repo_root / "original/include/curl/options.h",
            repo_root / "original/lib/easyoptions.c",
        ),
    }


def build_cve_manifest(repo_root: pathlib.Path) -> dict:
    curated = load_json(repo_root / "relevant_cves.json")
    curated_ids = {entry["cve_id"] for entry in curated["cves"]}
    patch_mappings = []
    for patch_path in sorted((repo_root / "original/debian/patches").glob("CVE-*.patch")):
        stem = patch_path.stem
        cve_id = stem
        suffix_match = re.match(r"^(CVE-\d{4}-\d+)(?:-(?:pre\d+|\d+))$", stem)
        if cve_id not in curated_ids and suffix_match:
            cve_id = suffix_match.group(1)
        patch_mappings.append(
            {
                "patch": str(patch_path.relative_to(repo_root)),
                "mapped_cve_id": cve_id,
                "match_found": cve_id in curated_ids,
            }
        )
    return {
        "schema_version": 1,
        "curated_relevant_cves": curated,
        "debian_patch_mappings": patch_mappings,
    }


def write_json(path: pathlib.Path, payload: dict) -> None:
    path.parent.mkdir(parents=True, exist_ok=True)
    path.write_text(json.dumps(payload, indent=2, sort_keys=True) + "\n")


def write_outputs(safe_dir: pathlib.Path, output_root: pathlib.Path) -> None:
    repo_root = safe_dir.parent
    abi_manifest = build_abi_manifest(repo_root, safe_dir)
    test_manifest = build_test_manifest(repo_root, safe_dir)
    cve_manifest = build_cve_manifest(repo_root)

    write_json(output_root / "metadata/abi-manifest.json", abi_manifest)
    write_json(output_root / "metadata/test-manifest.json", test_manifest)
    write_json(output_root / "metadata/cve-manifest.json", cve_manifest)

    openssl_symbols = parse_symbols(repo_root / "original/debian/libcurl4t64.symbols")
    gnutls_symbols = parse_symbols(repo_root / "original/debian/libcurl3t64-gnutls.symbols")
    (output_root / "abi").mkdir(parents=True, exist_ok=True)
    (output_root / "abi/libcurl-openssl.map").write_text(render_version_script(openssl_symbols))
    (output_root / "abi/libcurl-gnutls.map").write_text(render_version_script(gnutls_symbols))
    (output_root / "c_shim").mkdir(parents=True, exist_ok=True)
    (output_root / "c_shim/forwarders.c").write_text(render_forwarders(openssl_symbols["symbols"]))


def main() -> None:
    parser = argparse.ArgumentParser()
    parser.add_argument("--safe-dir", default=str(DEFAULT_SAFE_DIR))
    parser.add_argument("--output-root", default=None)
    args = parser.parse_args()

    safe_dir = pathlib.Path(args.safe_dir).resolve()
    output_root = pathlib.Path(args.output_root).resolve() if args.output_root else safe_dir
    write_outputs(safe_dir, output_root)


if __name__ == "__main__":
    main()
